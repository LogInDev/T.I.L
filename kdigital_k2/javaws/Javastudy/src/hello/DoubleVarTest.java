package hello;

public class DoubleVarTest {

	public static void main(String[] args) {
		//변수 선언
		double d0;
		float f0;
		
		//변수 초기화
		d0 = 3.14;
		f0 = 3.14F; //0f 소문자 가능
		
		//변수 선언과 초기화
		double d1 = 3.14;
		float f1 = 3.14F; // 1f 소문자 가능
		
		// 출력
		System.out.println(d0);
		System.out.println(f0);
		System.out.println(d1);
		System.out.println(f1);
				

	}

}
