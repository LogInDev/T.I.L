package hello;

public class CharVarTest {

	public static void main(String[] args) {
		//변수 선언
		char c0;
		
		//변수 초기화
		c0 = 'a';
		
		//변수 선언과 초기화
		char c1 = 'b';
		
		//출력
		System.out.println(c0);
		System.out.println(c1);
		
		//아스키 코드값을 넣어도 됨
		char c2 = 65;
	
		System.out.println(c2);
	
		

	}

}
