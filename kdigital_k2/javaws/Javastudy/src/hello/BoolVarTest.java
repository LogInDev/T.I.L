package hello;

public class BoolVarTest {

	public static void main(String[] args) {
		//변수 선언
		boolean t0;
		boolean f0;
		
		//변수 초기화
		t0 = true;
		f0 = false;
		
		//변수 선언과 초기화
		boolean t1 = true;
		boolean f1 = false;
		
		//출력
		System.out.println(t0);
		System.out.println(f0);
		System.out.println(t1);
		System.out.println(f1);

	}

}
